package zadanie72;

public class Zadanie72
{
    public static void main(String[] args)
    {
        final int size = 7;
        for (int row = 0; row < size; row ++)
        {
            for (int col = 7; col > (row); col--)
            {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}


      
       
      

